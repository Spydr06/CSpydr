void init_c_cg(CCodegenData_T* cg, Context_T* context, ASTProg_T* ast)
{
    memset(cg, 0, sizeof(struct C_CODEGEN_DATA_STRUCT));

    cg->context = context;
    cg->ast = ast;
    cg->silent = context->flags.silent;
    cg->code_buffer = open_memstream(&cg->buf, &cg->buf_len);
    cg->unique_id = 0;
    cg->blocks = init_list();
    cg->arrays = hashmap_init();
    cg->anon_structs = hashmap_init();
}

void free_c_cg(CCodegenData_T* cg)
{
    free(cg->buf);
    free_list(cg->blocks);

    List_T* arrays = hashmap_keys(cg->arrays);
    for(size_t i = 0; i < arrays->size; i++)
        free(arrays->items[i]);
    free_list(arrays);

    hashmap_free(cg->arrays);
    hashmap_free(cg->anon_structs);
}

